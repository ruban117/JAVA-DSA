public class JavaVariables {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(a);
        System.out.println(b);
        String name="Ruban Pathak";
        System.out.println(name);
        a=50;
        System.out.println(a);
        a=b;
        System.out.println(b);
    }
}
